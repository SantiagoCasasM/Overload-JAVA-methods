public class jcSobreCargaMetodos {
    public int Suma(int N1)
    {
        return N1+N1;
    }
    public int Suma(int N1, int N2)
    {
        return N1+N2;
    }
     public int Suma(int N1, int N2, int N3)
    {
        return N1+N2+N3;
    }
      public double Suma(double N1, int N2, int N3)
    {
        return N1+N2+N3;
    }
    public int Multiplicacion(int N1)
    {
        return N1*N1;
    }
    public int Multiplicacion(int N1, int N2)
    {
        return N1*N2;
    }
     public int Multiplicacion(int N1, int N2, int N3)
    {
        return N1*N2*N3;
    }
      public double Multiplicacion(double N1, int N2, int N3)
    {
        return N1*N2*N3;
    }
      
}
